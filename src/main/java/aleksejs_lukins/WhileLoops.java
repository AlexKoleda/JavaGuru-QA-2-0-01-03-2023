package aleksejs_lukins;

public class WhileLoops {
    public static void main(String[] args) {
        int a = 0;
        while (a < 21) {
            a++;
            System.out.print(a + " ");
            System.out.print(a * a + " ");
            System.out.println(a * a * a);
        }
        System.out.println("The end");

    }
}
