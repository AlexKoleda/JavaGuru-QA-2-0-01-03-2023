package aleksejs_lukins;

public class ForLoops {
    public static void main(String[] args) {
        for (int a = 1; a < 21; a++){
            a++;
            System.out.print(a + " ");
            System.out.print(a * a + " ");
            System.out.println(a * a * a);
        }
        System.out.println("The end");

    }
}
